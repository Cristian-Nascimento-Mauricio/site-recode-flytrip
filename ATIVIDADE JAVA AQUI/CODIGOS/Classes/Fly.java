package Classes;

public class Fly {
	
	private int id;
	private String from;
	private String to;
	private String scale;
	private double price;
	private String sand_company;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public String getTo() {
		return to;
	}
	public void setTo(String to) {
		this.to = to;
	}
	public String getScale() {
		return scale;
	}
	public void setScale(String scale) {
		this.scale = scale;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public String getSand_company() {
		return sand_company;
	}
	public void setSand_company(String sand_company) {
		this.sand_company = sand_company;
	}
	
	

}
